/**
 * AI解析自然语言输入
 * @param {string} input - 用户输入的自然语言文本
 * @param {string} userRole - 用户角色: 'supplier' | 'buyer' | ''
 * @returns {Object} 结构化的解析结果
 */
export function aiParse(input, userRole = '') {
  if (!input || !input.trim()) {
    return {
      success: false,
      message: '请输入内容',
      result: null
    };
  }

  const text = input.trim().toLowerCase();
  const result = {
    direction: '',       // 'supply' | 'demand'
    materialType: '',    // 'waste' | 'recycled'
    category: '',        // 标准化品类名
    confidence: 0,       // 综合置信度 0-1
    quantity: null,      // 数量（吨）
    price: null,         // 单价（元/吨）
    location: '',        // 地点
    form: '',            // 形态
    original: input,     // 原始输入
    interpreted: '',     // 系统解读
    details: {},         // 详细信息
  };

  // Step 1: 判断供应还是需求
  result.direction = detectDirection(text, userRole);

  // Step 2: 判断废料还是再生料
  result.materialType = detectMaterialType(text);

  // Step 3: 翻译行业黑话
  const normalized = translateBlackSpeech(text);

  // Step 4: 匹配品类分类体系
  const taxonomyMatch = matchTaxonomy(normalized, result.materialType);
  if (taxonomyMatch) {
    result.category = taxonomyMatch.name;
    result.confidence = taxonomyMatch.confidence || 0.7;
  }

  // Step 5: 提取数量、价格、地点
  result.quantity = extractQuantity(text);
  result.price = extractPrice(text);
  result.location = extractLocation(text);
  result.form = extractForm(text);

  // 组合置信度
  let confCount = 0;
  if (result.category) confCount++;
  if (result.quantity) confCount++;
  if (result.price) confCount++;
  if (result.location) confCount++;
  result.confidence = Math.min(0.95, 0.4 + confCount * 0.12);

  // 生成系统解读
  result.interpreted = generateInterpretation(result);

  // 构建详情
  result.details = buildDetails(result);

  return {
    success: !!result.category,
    message: result.category ? '解析成功' : '未能识别品类，请补充信息',
    result
  };
}

/**
 * Step 1: 检测供需方向
 */
function detectDirection(text, userRole) {
  // 如果用户已选择角色，优先使用
  if (userRole === 'supplier') return 'supply';
  if (userRole === 'buyer') return 'demand';

  let supplyScore = 0;
  let demandScore = 0;

  for (const kw of SUPPLY_KEYWORDS) {
    if (text.includes(kw)) supplyScore++;
  }
  for (const kw of DEMAND_KEYWORDS) {
    if (text.includes(kw)) demandScore++;
  }

  if (supplyScore > demandScore) return 'supply';
  if (demandScore > supplyScore) return 'demand';
  return 'supply'; // 默认认为是供应
}

/**
 * Step 2: 判断废料还是再生料
 */
function detectMaterialType(text) {
  // 检查再生料关键词
  const recycledPatterns = [
    '再生', '回料', '颗粒', '粒子', '一级', '二级', '特级',
    '再生颗粒', '再生料', '改性', '抽粒', '造粒',
  ];
  for (const p of recycledPatterns) {
    if (text.includes(p)) return 'recycled';
  }

  // 检查废料关键词
  const wastePatterns = [
    '废', '瓶片', '瓶砖', '破碎料', '粉碎料', '膜', '打包带',
    '编织袋', '桶料', '管料', '壳料', '片料', '废塑料',
  ];
  for (const p of wastePatterns) {
    if (text.includes(p)) return 'waste';
  }

  return 'waste'; // 默认废料
}

/**
 * Step 3: 翻译行业黑话
 */
function translateBlackSpeech(text) {
  let result = text;
  // 按黑话长度降序排列，优先替换长匹配
  const entries = Object.entries(BLACK_SPEECH_MAP).sort(
    (a, b) => b[0].length - a[0].length
  );
  for (const [slang, standard] of entries) {
    if (result.includes(slang)) {
      result = result.replace(new RegExp(slang, 'g'), standard);
    }
  }
  return result;
}

/**
 * Step 4: 匹配分类体系
 */
function matchTaxonomy(text, materialType) {
  const taxonomy = materialType === 'recycled' ? RECYCLED_TAXONOMY : WASTE_TAXONOMY;
  const entries = Object.entries(taxonomy).sort(
    (a, b) => b[0].length - a[0].length
  );

  let bestMatch = null;
  let bestLength = 0;

  for (const [key, value] of entries) {
    if (text.includes(key) && key.length > bestLength) {
      bestMatch = { ...value, confidence: Math.min(0.95, 0.5 + key.length * 0.1) };
      bestLength = key.length;
    }
  }

  // 如果没匹配到，尝试反向在品类名中搜索
  if (!bestMatch) {
    for (const [key, value] of entries) {
      // 拆分品类名中的词
      const parts = value.name.replace(/[再生颗粒一级二级三级特级]/g, '').split('');
      let matchCount = 0;
      for (const part of parts) {
        if (text.includes(part.toLowerCase())) matchCount++;
      }
      if (matchCount >= 2 && value.name.length > bestLength) {
        bestMatch = { ...value, confidence: 0.4 + matchCount * 0.1 };
        bestLength = value.name.length;
      }
    }
  }

  return bestMatch;
}

/**
 * Step 5a: 提取数量
 */
function extractQuantity(text) {
  // 匹配模式: XX吨, XX公斤, XXkg, XX t
  const patterns = [
    /(\d+\.?\d*)\s*吨/i,
    /(\d+\.?\d*)\s*[tT](?!\w)/,
    /(\d+\.?\d*)\s*公吨/i,
    /(\d+\.?\d*)\s*公斤/i,
    /(\d+\.?\d*)\s*[kK][gG]/,
  ];

  for (const pattern of patterns) {
    const match = text.match(pattern);
    if (match) {
      let value = parseFloat(match[1]);
      // 如果是公斤，转换为吨
      if (pattern.source.includes('公斤') || pattern.source.includes('kg')) {
        value = value / 1000;
      }
      return parseFloat(value.toFixed(2));
    }
  }

  // 尝试匹配 "30吨" 这类更宽松的模式
  const loose = text.match(/(\d{1,5})\s*(?:吨|t)\b/i);
  if (loose) return parseFloat(loose[1]);

  return null;
}

/**
 * Step 5b: 提取价格
 */
function extractPrice(text) {
  // 匹配模式: XX/吨, XX元/吨, ¥XX, XX出, XX/吨出
  const patterns = [
    /(\d{3,5})\s*(?:元)?[\/\s]*吨(?:\s*(?:出|卖|售))?/,
    /[¥￥]\s*(\d{3,5})/,
    /(\d{3,5})\s*(?:出|卖|售)(?!\w)/,
    /价格[：:]\s*(\d{3,5})/,
    /单价[：:]\s*(\d{3,5})/,
    /报价[：:]\s*(\d{3,5})/,
  ];

  for (const pattern of patterns) {
    const match = text.match(pattern);
    if (match) {
      const price = parseFloat(match[1]);
      // 合理性检查：废塑料/再生料单价通常在 1000-50000 范围
      if (price >= 100 && price <= 50000) {
        return price;
      }
    }
  }

  return null;
}

/**
 * Step 5c: 提取地点
 */
function extractLocation(text) {
  const entries = Object.entries(CITY_MAP).sort(
    (a, b) => b[0].length - a[0].length
  );

  for (const [cityName, info] of entries) {
    if (text.includes(cityName)) {
      return `${info.province}省${info.city}${info.province === info.city ? '市' : ''}`;
    }
  }

  // 尝试匹配 "XX省" 模式
  const provMatch = text.match(/([\u4e00-\u9fa5]{2,3})(?:省|市)/);
  if (provMatch) {
    const prov = provMatch[1];
    // 查找该省下的第一个城市
    for (const [cityName, info] of Object.entries(CITY_MAP)) {
      if (info.province === prov) {
        return `${info.province}省${info.city}${info.province === info.city ? '市' : ''}`;
      }
    }
    return `${prov}省`;
  }

  return '';
}

/**
 * Step 5d: 提取形态
 */
function extractForm(text) {
  const formPatterns = [
    { pattern: /瓶片|片片|片子/, form: '瓶片' },
    { pattern: /颗粒|粒子|颗颗|料子/, form: '颗粒' },
    { pattern: /破碎料|破料|破碎/, form: '破碎料' },
    { pattern: /粉碎料|粉碎|粉料/, form: '粉碎料' },
    { pattern: /瓶砖|瓶装/, form: '瓶砖' },
    { pattern: /膜|膜料/, form: '膜' },
    { pattern: /桶|桶料/, form: '桶料' },
    { pattern: /管料|管材/, form: '管材料' },
    { pattern: /打包带/, form: '打包带' },
    { pattern: /编织袋/, form: '编织袋' },
    { pattern: /壳|壳料/, form: '外壳料' },
  ];

  for (const { pattern, form } of formPatterns) {
    if (pattern.test(text)) return form;
  }

  return '';
}

/**
 * 生成用户可读的系统解读
 */
function generateInterpretation(result) {
  const parts = [];

  if (result.direction === 'supply') {
    parts.push('🧑‍🏭 您想【出售】');
  } else {
    parts.push('🏭 您想【采购】');
  }

  if (result.materialType === 'waste') {
    parts.push('废塑料');
  } else {
    parts.push('再生料');
  }

  if (result.category) {
    parts.push(`—— ${result.category}`);
  }

  if (result.quantity) {
    parts.push(`${result.quantity}吨`);
  }

  if (result.price) {
    parts.push(`单价¥${result.price}/吨`);
  }

  if (result.location) {
    parts.push(`📍${result.location}`);
  }

  if (result.form) {
    parts.push(`形态：${result.form}`);
  }

  return parts.join(' ');
}

/**
 * 构建详细信息
 */
function buildDetails(result) {
  const details = {
    direction: result.direction,
    directionLabel: result.direction === 'supply' ? '供应' : '需求',
    materialType: result.materialType,
    materialTypeLabel: result.materialType === 'waste' ? '废塑料' : '再生料',
    category: result.category,
    quantity: result.quantity,
    price: result.price,
    location: result.location,
    form: result.form,
    confidence: result.confidence,
    confidenceLabel: result.confidence >= 0.8 ? '高' : (result.confidence >= 0.5 ? '中' : '低'),
  };
  return details;
}

